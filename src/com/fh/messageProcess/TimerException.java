package com.fh.messageProcess;

public class TimerException extends Exception {

	private static final long serialVersionUID = 3856734821600433912L;

	public TimerException(String arg0) {
		super(arg0);
	}

	public TimerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TimerException(Throwable arg0) {
		super(arg0);
	}
}
